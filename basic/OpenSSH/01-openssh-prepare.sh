#!/bin/bash

${log} `basename "$0"` " started" blfs_basic &&

install  -v -m700 -d /var/lib/sshd &&
chown    -v root:sys /var/lib/sshd &&

groupadd -g 50 sshd        &&
useradd  -c 'sshd PrivSep' \
         -d /var/lib/sshd  \
         -g sshd           \
         -s /bin/false     \
         -u 50 sshd

${log} `basename "$0"` " finished" blfs_basic
